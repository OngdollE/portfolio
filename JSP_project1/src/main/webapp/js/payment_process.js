
    function showCardOptions() {
        document.getElementById('cardOptions').style.display = 'block';
        document.getElementById('phoneOptions').style.display = 'none';
        document.getElementById('bankOptions').style.display = 'none';
    }

    function showPhoneOptions() {
        document.getElementById('cardOptions').style.display = 'none';
        document.getElementById('phoneOptions').style.display = 'block';
        document.getElementById('bankOptions').style.display = 'none';
    }

    function showBankOptions() {
        document.getElementById('cardOptions').style.display = 'none';
        document.getElementById('phoneOptions').style.display = 'none';
        document.getElementById('bankOptions').style.display = 'block';
    }

   